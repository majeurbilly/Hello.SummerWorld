# Demande à l'utilisateur de saisir deux nombres
frist = input("Veuillez saisir un entier : ")
second = input("Veuillez saisir un autre entier : ")

# Convertit les saisies en entiers
frist = int(frist)
second = int(second)

# Multiplie les deux nombres
result = frist - second

# Affiche le résultat
print(result)
